                  <li>Time is measured from the time the robot crosses the starting line until the time it crosses the finish line. A robot is deemed to have crossed the line when the forward most wheel or leg of the robot contacts or crosses over the line.</li>
                  <li>A maximum of 5 minutes in first qualifying round and 10 minutes in other remaining rounds is allowed for a robot to complete the course. A robot that cannot complete the course in the allotted time shall be disqualified. Only best 10 robots out of all are selected after completion of first qualifying round.</li>
                  <li>Time shall be judge with a stopwatch.</li>
                  <li>A robot cross the arena surface will be disqualified, A robot shall be deemed to have left the arena when any wheel or leg has moved completely off the arena surface.</li>
                  <li>Any robot that loses the line course must reacquire the line at the point where it was lost, or at any earlier (e.g. Already traversed) point.</li>
                  <li>Any robot that loses the line course and fails to reacquire it will be allowed a single reattempt. The robot must start the course again from the beginning, and if it loses the line course on its second attempt it will be disqualified.</li>
                  <li>The decisions of all officials regarding these rules and the conduct of the event shall be final.</li>
