                <h3 class="text-primary">Rules</h3>
                <ul>
                  <li>A team may consist 2 or 4 members.</li>
                  <li>Only one robot is allowed per team.</li>
                  
                </ul>
                <h3 class="text-primary">Eligibility</h3>
                <ul>
                  <li>All students with a valid ID card of their respective educational institute are eligible to participate.</li>
                  <li>Only wireless and remote less (no external control) robots are eligible to participate.</li>
                  <li>No power supply will be provided.</li>
                  <li>The line shall be a 15 to 20mm wide black coloured with white background.</li>
