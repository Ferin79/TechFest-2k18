              <div class="tab-pane fade active in" id="tab-detail">
                <h3 class="text-primary">Event detail</h3>
                <p>
                  Ever heard of robot playing hockey? BOT Hockey is an event based on the controlling of robot while playing an innovative version of the sports game Ice Hockey. The event is a robotic event which consists of four rounds having various difficulties. Higher the rounds, higher are the difficulties.
                  Participants will be able to learn new technical skills and will be introduced with new mechanism and the team coordination will be the main factor they will be introduced.

                </p>
                <h3 class="text-primary">Levels</h3>
                <ul>
                  <p>There are three rounds in the whole event.</p>
                  <li>
                    Round-1
                    <ul>
                      <li>This round will be an individual robot round where each robot will have to score in minimum time from given position.                  
                      </li>
                      <li>This round will be an individual robot round where each robot will have to face difficulties; here time will be the judge.</li>
                      <li> Robot have to pass through hurdles in minimum time.</li>
                    </ul>
                  <li>
                    Round-2
                    <ul>
                      <li>The round will be called faceoff round, in this round two opponent team will have to play against each other and score more goal then opponent team, winner will qualify team to next round.
                      </li>                     
                    </ul>
                  <li>
                    Round-3
                    <ul>
                      <li>This round will be the final faceoff round, the winning team will be awarded with 1st price, and losing team will be awarded with 2nd price.
                      </li>                      
                    </ul>
                  </li>
                </ul>
                <h3 class="text-primary">Hurdle detail</h3>
                <p>
                  There will be hurdles every participant have to face down in each round, the hurdles mechanism will be situated on each goal post and the mechanism will be like rotating propeller or rotating fan with certain thickness
                </p>
                <h3 class="text-primary">Requirements</h3>
                <ul>
                  <li>Weighing Machines</li>
                  <li>Measurement Tape</li>
                  <li>Multimeter</li>
                </ul>
                <h3 class="text-primary">Robot specification</h3>
                <ul>
                  <li>The robot size must be 35cm*30cm*30cm in all direction.</li>
                  <li>The robot should not exceed 4kg of weight.</li>
                  <li>The robot is should be able to move on a smooth surface.</li>
                  <li>There should be a hitting mechanism attached to the robot. (The robot will not be allowed to drag the ball in any circumstances)</li>
                  <li>Potential at any point on the Robot should not exceed 12V. Participants cannot draw power from outside.</li>
                  <li>The robot can be powered electrically only. The use of IC engine in any form is not allowed.</li>
                  <li>The total length of wire extending from the remote control to the robot must be minimum of 4 to 4.5 meters</li>
                  <li>If the Robot is using an external wireless remote control, make sure that it has sufficient range and that it can control the machine well without any lag.</li>
                </ul>
              </div>
              <div class="tab-pane fade" id="tab-rules">
                <h3 class="text-primary">General Rules</h3>
                <ul>
                  <li>The basic rule of this event is “your robot must be able to hit the ball and the same robot should be able to throw/hit ball to the Goal post”.</li>
                  <li>If a participant is unable to hit a ball in the goal post in given time limit they will not be qualified to the next round.</li>
                  <li>Each participant must hit the ball.</li>
                  <li>Robots must have a hit mechanism. (Dragging the ball is not allowed.)</li>
                  <li>Participants are not allowed to touch or drag the robot for controlling purpose. If the robot falls down or start to malfunction, touches are allowed in such cases.</li>
                  <li>The robot must hit or throw the ball, being behind the starting line. If the line is crossed, penalty will be given.</li>
                  <li>Each match will be conducted by choosing random opponents.</li>
                  <li>All the required accessories have to be brought by the participants</li>
                  <li>The participants are not allowed to damage the track, or indulge in any argument with coordinator as this will lead to disqualification of their robot.</li>
                  <li>The batteries can be recharged only during the breaks.</li>
                  <li>Coordinators reserve the right to ask for the explanation of the robot.</li>
                </ul>
                <h3 class="text-primary">Evaluation Criterion</h3>
                <ul>
                  <li>Judging will be done by event coordinators.</li>
                  <li>It will be based on time criteria the best of the outcomes will be selected on the second round.</li>
                  <li>In the further rounds the participants will be introduced with the difficulties.</li>
                  <p>Next round based on the face of system and the coordinators will going to select the winning</p>
                </ul>
