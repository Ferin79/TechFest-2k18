              <div class="tab-pane fade" id="tab-rules">
                <h3 class="text-primary">General Rules</h3>
                <ul>
                  <li>The Robot will be inspected for safety before the event begins. If found to be unsafe for the participants/spectators, it will be discarded.</li>
                  <li>All the required accessories have to be brought by the participants..</li>
                  <li>The participants are not allowed to damage the track, or indulge in any argument with the coordinator as this will lead to disqualification of their robot.</li>
                  <li>Penalty will be given when robot moves out of arena.</li>
                  <li>The wireless remote-control sets must be handed over to the event coordinator at the time of reporting, so as to avoid any illegal control of the Robot due to interference caused by the intentional or unintentional matching of radio frequencies of different participants.</li>
                  <li>In case of wire entanglement, the timers will not be stopped and the race will be restarted from the previous checkpoint. In case any discrepancy, decision of the organizing panel will be considered absolute.</li>
                  <li>The organizers reserve the right to change any or all of the rules as they deem fit. Changes in rules, if any, will be highlighted on the website.</li>
                  <li>Violation of any of the rules will result in immediate disqualification.</li>
                  <li>The maximum time limit for the Completion of Task inside the arena is 10 minutes.</li>
                  <li>Teams that are not ready when called for turn will be considered to have declared a walkover, and will receive no points.</li>
                  <li>In all cases, the judge’s decision will be final and binding.</li>
                  <li>The operator of the Robot is not allowed to step in the arena.</li>
                </ul>
                <h3 class="text-primary">Robot specification</h3>
                <ul>
                  <li>The maximum dimensions of the robot are 35 cm x 30 cm x 30 cm (l x b x h) (WITH MECHANISM) and maximum weight is 4.5 kg.</li>
                  <li>Robots should not be constructed using ready-made Lego kits or any ready-made mechanism but use of ready-made gear assemblies is allowed.</li>
                  <li>Potential at any point on the robot should not exceed 12V. Participants cannot draw power from outside source.</li>
                  <li>The Robot can be controlled manually or it can be made semi-automatic or automatic can be powered electrically only. The use of IC engine in any form is not allowed.</li>
                  <li>In case of wired mechanism, the wire must be slack at any point of time during the game.</li>
                  <li>The total length of wire extending from the remote control to the robot must be minimum of 4 to 4.5 meters</li>
                  <li>If the Robot is using an external wireless remote control, make sure that it has sufficient range and that it can control the machine well without any lag.</li>
                  <li>The robot should have the mechanism/object to burst the balloon.</li>              

                </ul>
                <h3 class="text-primary">Evaluation Criterion</h3>
                <ul>
                  <li>Judging will be done by event coordinators.</li>
                  <li>Penalties will be given if Hand touched while bursting , waste of time at a particular spot, if participants robot crosses the maze lines, if any type of external help done by participants to robot like pulling the robot backwards using wires connection.</li>
                  <li>It will be based on the number of balloon bursted by a particular team as well as minus points (penalties) which will be decided later on.</li>
                  <li>The time will also be a major factor in case the tie occurs in the point system.</li>
                  <li>Decision made by the event head will be final and acceptable in any cases.</li>
                  <li>The decision of the coordinator will be final and the rules may vary for the successful complication of the event.</li>
                  <li>Certificate of participation will be given to all participants.</li>
                </ul>
